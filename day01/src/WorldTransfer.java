public class WorldTransfer {
    public static  void main(String[] args){
        // \n  换行符
        System.out.println("北京天津上海");
        System.out.println("北京\n天津\n上海");

        //  \t 一个制表位，实现对齐功能
        System.out.println("北京天津上海");
        System.out.println("北京\t天津\t上海");

        // \\ 一个\
        System.out.println("\\我\\你\\他");

        //  \"  一个“
        System.out.println("妈妈说:\"好哈学习。\"");
        //  \'  一个‘
        System.out.println("妈妈说的要\'好哈工作\'");

        //  \r   一个回车

        System.out.println("书名\t作者\t价格\t销量\n三国\t罗贯中\t12345678\tGHG");
        System.out.println("\t120\t1000");

        /*
        乘法
         */


    }


}
